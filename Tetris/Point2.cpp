#include "Point2.h"

///-------------------------------------------------------------------------------------------------
/// <summary> Default constructor. </summary>
/// <remarks> Wolf, 08-Nov-15. </remarks>
///-------------------------------------------------------------------------------------------------
Point2::Point2( )
{
    this->X = 0;
    this->Y = 0;
}

///-------------------------------------------------------------------------------------------------
/// <summary> Constructor. </summary>
/// <remarks> Wolf, 08-Nov-15. </remarks>
/// <param name="X"> The X coordinate. </param>
/// <param name="Y"> The Y coordinate. </param>
///-------------------------------------------------------------------------------------------------
Point2::Point2( int X, int Y )
{
    this->X = X;
    this->Y = Y;
}